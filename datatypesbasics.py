# Data types:
#   Integer         -       int         -           Whole numbers such as 3, 300, 200
#   floating point  -       float       -           Numbers with decimal point: 2.3, 4.6, 100.0
#   complex         -       complex     -           Complex numbers represent mathematical entities with a real part and an imaginary part. The imaginary unit is denoted by the letter j in Python. Complex numbers are written in the form a + bj, where a is the real part and b is the imaginary part.
#   Strings         -       str         -           Ordered sequence of characters: "hello", "Sammy", "2000", "asdasd"
#   Booleans        -       bool        -           Logical value indicating True or False

# Data structures
# Data structures act like containers that store collections of data items.   
#   List            -       list        -           Ordered sequence of objects: [10,"hello",200.3]
#   Dictionaries    -       Dict        -           Unordered Key:Value pairs:  {"mykey":"value","name","Frankie"}
#   Tuples          -       tup         -           Ordered immutable sequence of objects: (10,"hello", 200.3)
#   Sets            -       set         -           Unordered collection of unique objects: {"a","b"}