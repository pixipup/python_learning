#Comparison operators:-

#Equal                          -           "=="
print(1==2)
#Not Equal                      -           "!="
print(1!=2)
#Greater than                   -           ">"
print(1>2)
#Less than                      -           "<"
print(1<2)
#Greater than or equal to       -           ">="
print(1>=1)
#Less than or equal to          -           "<="
print(1<=2)

#Logical operators:-

    #and
    #or
    #not

print(1<=4 and 2>3) # both conditions should be true in order to return true
print("h" == "h" or 4>5) # one of the condition should be true in order to return true
print(not 1<2) #Return the opposite boolean